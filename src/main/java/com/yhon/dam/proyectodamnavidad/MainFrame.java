                session.persist(new Recompensa("Elegir la cena", 400));
                session.persist(new Recompensa("Cenar fuera", 1000));
                session.persist(new Recompensa("Pedir comida a domicilio", 1000));
                session.persist(new Recompensa("Helado o dulce especial", 200));
                session.persist(new Recompensa("Desayuno especial", 300));
