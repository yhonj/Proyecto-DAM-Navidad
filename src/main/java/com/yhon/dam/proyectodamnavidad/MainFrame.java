    /**
     * Maneja el evento de inicio de sesión.
     * <p>
     * Valida las credenciales introducidas y, según el resultado, permite el
     * acceso a la aplicación o muestra un mensaje de error.
     * </p>
     *
     * @param evt evento de acción generado al pulsar el botón
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());

        LoginControlador controller = new LoginControlador();
        LoginResult result = controller.login(username, password);

        switch (result) {
            case OK:
                Usuario usuario = controller.getUsuarioLogueado();

                TareasVista vista = new TareasVista(usuario);
                vista.setLocationRelativeTo(this);
                vista.setVisible(true);

                this.dispose(); // cerrar login
                break;

            case USER_NOT_FOUND:
                Object[] options = {I18n.t("common.ok")};

                JOptionPane.showOptionDialog(
                        this,
                        I18n.t("login.userNotFound"),
                        I18n.t("login.error"),
                        JOptionPane.DEFAULT_OPTION,
                        JOptionPane.WARNING_MESSAGE,
                        null,
                        options,
                        options[0]
                );
                break;

            case WRONG_PASSWORD:
                Object[] options2 = {I18n.t("common.ok")};

                JOptionPane.showOptionDialog(
                        this,
                        I18n.t("login.wrongPassword"),
                        I18n.t("login.error"),
                        JOptionPane.DEFAULT_OPTION,
                        JOptionPane.ERROR_MESSAGE,
                        null,
                        options2,
                        options2[0]
                );
                break;
        }

    }//GEN-LAST:event_jButton1ActionPerformed

