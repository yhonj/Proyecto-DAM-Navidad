     /**
     * Guarda la nueva tarea tras validar los datos introducidos.
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int puntos = (int) spPuntos.getValue();

        String nombre = txtNombre.getText().trim();
        String descripcion = txtDescripcion.getText();
        Usuario responsable = (Usuario) comboResponsable.getSelectedItem();
        Date inicio = dateInicio.getDate();
        Date fin = dateFin.getDate();

        if (nombre.isEmpty() || responsable == null || inicio == null || fin == null) {
            JOptionPane.showMessageDialog(this,
                    I18n.t("task.create.required"),
                    I18n.t("common.error"),
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        Tarea tarea = new Tarea(
                nombre,
                descripcion,
                usuarioLogueado,
                responsable,
                inicio.toInstant().atZone(ZoneId.systemDefault()).toLocalDate(),
                fin.toInstant().atZone(ZoneId.systemDefault()).toLocalDate()
        );

        // üëá AQU√ç se asignan los puntos (CORRECTO)
        tarea.setPuntosRecompensa(puntos);

        Object[] okOption = {I18n.t("common.ok")};

        if (TareaDAO.guardar(tarea)) {

            JOptionPane.showOptionDialog(
                    this,
                    I18n.t("task.create.success"),
                    I18n.t("common.success"),
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.INFORMATION_MESSAGE,
                    null,
                    okOption,
                    okOption[0]
            );

            vistaPadre.cargarTareas(); // refrescar tabla
            this.dispose(); // cerrar ventana

        } else {

            JOptionPane.showOptionDialog(
                    this,
                    I18n.t("task.create.error"),
                    I18n.t("common.error"),
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.ERROR_MESSAGE,
                    null,
                    okOption,
                    okOption[0]
            );
        }

    }//GEN-LAST:event_jButton1ActionPerformed
    
