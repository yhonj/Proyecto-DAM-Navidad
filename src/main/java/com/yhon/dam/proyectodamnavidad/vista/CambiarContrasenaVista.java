     /**
     * Gestiona el proceso de cambio de contrase√±a.
     * <p>
     * Valida los campos, comprueba la contrase√±a antigua,
     * genera el nuevo hash y lo guarda en la base de datos.
     * </p>
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        char[] oldPass = jPasswordField2.getPassword();
        char[] newPass = jPasswordField1.getPassword();

        Object[] okOption = {I18n.t("common.ok")};

        if (oldPass.length == 0 || newPass.length == 0) {

            JOptionPane.showOptionDialog(
                    this,
                    I18n.t("password.empty"),
                    I18n.t("common.warning"),
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.WARNING_MESSAGE,
                    null,
                    okOption,
                    okOption[0]
            );
            return;
        }

        // üîê Comprobar contrase√±a antigua
        if (!PasswordUtil.checkPassword(
                new String(oldPass),
                usuarioLogueado.getPasswordHash()
        )) {

            JOptionPane.showOptionDialog(
                    this,
                    I18n.t("password.oldIncorrect"),
                    I18n.t("common.error"),
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.ERROR_MESSAGE,
                    null,
                    okOption,
                    okOption[0]
            );
            return;
        }

        // üîí Generar hash nueva contrase√±a
        String newHash = PasswordUtil.hashPassword(new String(newPass));

        // üíæ Guardar en BD
        UsuarioDAO.actualizarPassword(usuarioLogueado, newHash);

        // üîÑ Actualizar objeto en memoria
        usuarioLogueado.setPasswordHash(newHash);

        JOptionPane.showOptionDialog(
                this,
                I18n.t("password.changed"),
                I18n.t("common.success"),
                JOptionPane.DEFAULT_OPTION,
                JOptionPane.INFORMATION_MESSAGE,
                null,
                okOption,
                okOption[0]
        );

        // üßπ Limpiar arrays por seguridad
        java.util.Arrays.fill(oldPass, '\0');
        java.util.Arrays.fill(newPass, '\0');

        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed
    
