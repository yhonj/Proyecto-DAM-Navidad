    void cargarTareas() {

        List<Tarea> tareas = TareaDAO.listarPorResponsable(usuarioLogueado);

        modeloTabla.setRowCount(0);

        for (Tarea t : tareas) {
            modeloTabla.addRow(new Object[]{
                t, // 👈 objeto completo
                t.getNombre(),
                t.getDescripcion(),
                t.getAsignadoPor().getUsername(),
                t.getResponsable().getUsername(),
                t.getFechaInicio(),
                t.getFechaLimite(),
                t.getEstado(),
                false
            });
        }
    }
