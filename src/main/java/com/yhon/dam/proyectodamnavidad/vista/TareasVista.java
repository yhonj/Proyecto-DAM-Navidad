    private void inicializarTabla() {
        modeloTabla = new DefaultTableModel(
                new Object[]{
                    "OBJ", // OCULTA
                    "Nombre",
                    "Descripción",
                    "Asignado por",
                    "Responsable",
                    "Fecha inicio",
                    "Fecha límite",
                    "Estado",
                    "Finalizar"
                }, 0
        ) {
            @Override
            public Class<?> getColumnClass(int columnIndex) {
                return columnIndex == COLUMNA_CHECKBOX
                        ? Boolean.class
                        : Object.class;
            }

            @Override
            public boolean isCellEditable(int row, int column) {
                return column == COLUMNA_CHECKBOX;
            }
        };

        tablaTareas.setModel(modeloTabla);
        tablaTareas.getColumnModel().getColumn(0).setMinWidth(0);
        tablaTareas.getColumnModel().getColumn(0).setMaxWidth(0);
        tablaTareas.getColumnModel().getColumn(0).setWidth(0);

    }

    private void cargarDatosUsuario() {
        lblUsuario.setText("Usuario: " + usuarioLogueado.getUsername());
        lblEmail.setText("Email: " + usuarioLogueado.getEmail());
    }

    private void añadirListenerTabla() {
        modeloTabla.addTableModelListener(e -> {

            if (e.getType() != TableModelEvent.UPDATE) {
                return;
            }

            int fila = e.getFirstRow();
            int columna = e.getColumn();

            if (columna == COLUMNA_CHECKBOX) {

                Boolean marcada = (Boolean) modeloTabla.getValueAt(fila, columna);

                if (Boolean.TRUE.equals(marcada)) {

                    Tarea tarea = (Tarea) modeloTabla.getValueAt(fila, COL_TAREA_OBJ);

                    TareaDAO.marcarFinalizada(tarea);

                    modeloTabla.setValueAt("FINALIZADA", fila, 7);
                }
            }
        });

    }

