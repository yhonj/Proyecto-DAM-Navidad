    /**
     * Gestiona el canjeo de una recompensa seleccionada.
     * <p>
     * Comprueba la selecci贸n, confirma la acci贸n y valida
     * si el usuario dispone de puntos suficientes.
     * </p>
     */
    private void btnCanjearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCanjearActionPerformed
        int fila = tablaRecompensas.getSelectedRow();

        //  Opciones traducidas
        Object[] okOption = {I18n.t("common.ok")};
        Object[] yesNoOptions = {
            I18n.t("option.no"),
            I18n.t("option.yes")
        };

        // No hay selecci贸n
        if (fila == -1) {
            JOptionPane.showOptionDialog(
                    this,
                    I18n.t("reward.select"),
                    I18n.t("common.warning"),
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.WARNING_MESSAGE,
                    null,
                    okOption,
                    okOption[0]
            );
            return;
        }

        Recompensa r = (Recompensa) modeloTabla.getValueAt(fila, 0);

        // Texto de confirmaci贸n (CORRECTO)
        String mensajeConfirmacion = String.format(
                I18n.t("reward.confirm"),
                r.getNombre(),
                r.getCostePuntos()
        );

        int confirm = JOptionPane.showOptionDialog(
                this,
                mensajeConfirmacion,
                I18n.t("reward.confirm.title"),
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null,
                yesNoOptions,
                yesNoOptions[0]
        );

        if (confirm != 1) { // 1 = YES
            return;
        }

        // 3锔 Intentar canjear
        boolean ok = TareaDAO.canjearRecompensa(usuarioLogueado, r);

        if (ok) {

            JOptionPane.showOptionDialog(
                    this,
                    I18n.t("reward.success"),
                    I18n.t("common.success"),
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.INFORMATION_MESSAGE,
                    null,
                    okOption,
                    okOption[0]
            );

            vistaPadre.refrescarUsuario();
            dispose();

        } else {

            JOptionPane.showOptionDialog(
                    this,
                    I18n.t("reward.notEnoughPoints"),
                    I18n.t("common.error"),
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.ERROR_MESSAGE,
                    null,
                    okOption,
                    okOption[0]
            );
        }
    }//GEN-LAST:event_btnCanjearActionPerformed
    
