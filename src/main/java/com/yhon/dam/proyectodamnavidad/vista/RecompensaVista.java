     /**
     * Inicializa el modelo de la tabla y oculta la columna de objetos.
     */
    private void inicializarTabla() {
        modeloTabla = (DefaultTableModel) tablaRecompensas.getModel();

        // Ocultar columna OBJ
        tablaRecompensas.getColumnModel().getColumn(0).setMinWidth(0);
        tablaRecompensas.getColumnModel().getColumn(0).setMaxWidth(0);
        tablaRecompensas.getColumnModel().getColumn(0).setWidth(0);
    }

     /**
     * Carga las recompensas desde la base de datos.
     */
    private void cargarRecompensas() {
        modeloTabla.setRowCount(0);

        for (Recompensa r : RecompensaDAO.listar()) {
            // Traducir el nombre de la recompensa antes de agregarlo a la tabla
            String nombreTraducido = I18n.getRewardTranslation(r.getNombre());

            modeloTabla.addRow(new Object[]{
                r, // Objeto completo
                nombreTraducido, // Nombre traducido
                r.getCostePuntos() // Coste en puntos
            });
        }
    }
    
     /**
     * Configura el listener de selecci贸n de la tabla
     * para habilitar o deshabilitar el bot贸n de canjeo.
     */
    private void configurarListenerTabla() {
        tablaRecompensas.getSelectionModel().addListSelectionListener(e -> {
            if (e.getValueIsAdjusting()) {
                return;
            }

            int fila = tablaRecompensas.getSelectedRow();
            if (fila == -1) {
                btnCanjear.setEnabled(false);
                return;
            }

            Recompensa r = (Recompensa) modeloTabla.getValueAt(fila, 0);
            btnCanjear.setEnabled(usuarioLogueado.getPuntos() >= r.getCostePuntos());
        });
    }

